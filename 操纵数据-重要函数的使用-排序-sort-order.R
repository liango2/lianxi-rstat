# 介绍
#方法1: sort函数:  返回排序后的内容 
#方法2: order函数: 返回排序后的内容的原先下标

################################################################################
# 例子: 数据准备
    x <- data.frame(v1 = 1:5, v2 = c(10, 7, 9, 6, 8), v3 = 11:15, v4 = c(1, 1, 2, 2, 1))
    x
    #  v1 v2 v3 v4
    #1  1 10 11  1
    #2  2  7 12  1
    #3  3  9 13  2
    #4  4  6 14  2
    #5  5  8 15  1

################################################################################
# 例子: sort
    sort(x$v2) #默认是升序排列
    #[1]  6  7  8  9 10

    sort(x$v2, decreasing = TRUE) #降序排列
    [1] 10  9  8  7  6

################################################################################
# 例子: order
     order(x$v2)
    [1] 4 2 5 3 1
    x[order(x$v2),] #按指定行号来显示
      v1 v2 v3 v4
    4  4  6 14  2
    2  2  7 12  1
    5  5  8 15  1
    3  3  9 13  2
    1  1 10 11  1
################################################################################
    order(x$v4, x$v2) #先安v4的值大小来排序，如果v4相同再按照v2值大小来排序
    [1] 2 5 1 4 3

    x[order(x$v4, x$v2),]
      v1 v2 v3 v4
    2  2  7 12  1
    5  5  8 15  1
    1  1 10 11  1
    4  4  6 14  2
    3  3  9 13  2 
